#!/usr/bin/env node
// One-time generator: fetches the public Google Fonts metadata endpoint and
// writes src/shared/googleFontsCatalog.ts so the web part needs no runtime
// API calls or API keys. A .ts module (instead of .json) keeps both the SPFx
// and Vite builds happy without resolveJsonModule configuration.
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { writeFile } from 'node:fs/promises';

const metadataUrl = 'https://fonts.google.com/metadata/fonts';
const outputPath = path.join(
  path.dirname(fileURLToPath(import.meta.url)),
  '..',
  'src',
  'shared',
  'googleFontsCatalog.ts'
);

const response = await fetch(metadataUrl);
if (!response.ok) {
  throw new Error(`Failed to fetch Google Fonts metadata: ${response.status} ${response.statusText}`);
}

// The endpoint historically prefixes the payload with ")]}'" to prevent JSON hijacking.
const raw = (await response.text()).replace(/^\)\]\}'/, '');
const metadata = JSON.parse(raw);
const familyList = metadata.familyMetadataList;

if (!Array.isArray(familyList) || familyList.length === 0) {
  throw new Error('Unexpected Google Fonts metadata shape: familyMetadataList missing.');
}

const families = familyList
  .map((family) => ({
    family: family.family,
    category: (family.category || '').toLowerCase(),
    weights: [
      ...new Set(
        Object.keys(family.fonts || {})
          .map((variant) => Number.parseInt(variant, 10))
          .filter((weight) => Number.isFinite(weight))
      )
    ].sort((a, b) => a - b),
    italic: Object.keys(family.fonts || {}).some((variant) => variant.endsWith('i'))
  }))
  .filter((family) => family.family)
  .sort((a, b) => a.family.localeCompare(b.family));

const entries = families
  .map((family) => `  ${JSON.stringify(family)}`)
  .join(',\n');

const source = `// Generated by scripts/generate-google-fonts-catalog.mjs on ${new Date().toISOString().slice(0, 10)}.
// Do not edit by hand; re-run the script to refresh the catalog.

export interface GoogleFontFamily {
  family: string;
  category: string;
  weights: number[];
  italic: boolean;
}

export const googleFontsCatalog: GoogleFontFamily[] = [
${entries}
];
`;

await writeFile(outputPath, source);
console.log(`Wrote ${families.length} font families to ${path.relative(process.cwd(), outputPath)}`);
